document.getElementById("searchInput").addEventListener("keydown", function(e) {
  if (e.key === "Enter") {
    e.preventDefault();

    const keyword = this.value.toLowerCase();
    cariWisata(keyword);
  }
});


function cariWisata() {
    let keyword = document.getElementById("searchInput").value.toLowerCase();
    let hasil = document.getElementById("hasilWisata");

    hasil.innerHTML = ""; // bersihkan hasil sebelumnya

    for (let key in wisata) {
        let data = wisata[key];

        if (data.nama.toLowerCase().includes(keyword)) {
            hasil.innerHTML += `
                <div class="card">
                    <img src="${data.gambar}" alt="${data.nama}">
                    <h3>${data.nama}</h3>
                    <p>${data.deskripsi}</p>
                    <p><b>Tiket:</b> ${data.tiket}</p>
                    <p><b>Lokasi:</b> ${data.lokasi}</p>
                </div>
            `;
        }
    }

    if (hasil.innerHTML === "") {
        hasil.innerHTML = "<p>Data tidak ditemukan</p>";
    }
}

